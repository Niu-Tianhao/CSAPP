/* 
 * conditional - same as x ? y : z 
 *   Example: conditional(2,4,5) = 4
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
int conditional(int x, int y, int z) {
  int mask;
  mask = ~(!(!x)) + 1;
  return ((mask & y) | (~mask & z));
}
/* 
 * isNonNegative - return 1 if x >= 0, return 0 otherwise 
 *   Example: isNonNegative(-1) = 0.  isNonNegative(0) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 3
 */
int isNonNegative(int x) {
  return (x >> 31) + 1;
  // 右移31位,取符号位
  // 如果x为负数,则结果为-1,加1后为0
  // 如果x为非负数,则结果为0,加1后为1
}
/* 
 * isGreater - if x > y  then return 1, else return 0 
 *   Example: isGreater(4,5) = 0, isGreater(5,4) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isGreater(int x, int y) {
  int sx, sy, diff, ds, sign_diff;
  sx = (x >> 31) & 1;
  sy = (y >> 31) & 1;
  diff = x + (~y + 1);
  ds = (diff >> 31) & 1;
  sign_diff = sx ^ sy;
  return (sign_diff & !sx) | (!sign_diff & !ds & !!diff);
}

/* 
 * absVal - absolute value of x
 *   Example: absVal(-1) = 1.
 *   You may assume -TMax <= x <= TMax
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 4
 */
int absVal(int x) {
  int mask, neg_x, abs_x;
  mask = x >> 31;
  neg_x = x + mask;
  abs_x = neg_x ^ mask;
  return abs_x;
}
/*
 * isPower2 - returns 1 if x is a power of 2, and 0 otherwise
 *   Examples: isPower2(5) = 0, isPower2(8) = 1, isPower2(0) = 0
 *   Note that no negative number is a power of 2.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 4
 */
int isPower2(int x) {
  int sign, is_zero, mask;
  sign = x >> 31;
  is_zero = !x;
  mask = x & (x + ~0);  // x & (x - 1)
  return (!sign & !is_zero & !mask);
}
/* 
 * float_neg - Return bit-level equivalent of expression -f for
 *   floating point argument f.
 *   Both the argument and result are passed as unsigned int's, but
 *   they are to be interpreted as the bit-level representations of
 *   single-precision floating point values.
 *   When argument is NaN, return argument.
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 10
 *   Rating: 2
 */
unsigned float_neg(unsigned uf) {
  unsigned sign, exp, frac;
  sign = uf & 0x80000000;
  exp = uf & 0x7F800000;
  frac = uf & 0x007FFFFF;
  if (exp == 0x7F800000 && frac != 0) {
    return uf; // NaN
  }
  return sign ^ 0x80000000 | exp | frac;
}
/* 
 * float_i2f - Return bit-level equivalent of expression (float) x
 *   Result is returned as unsigned int, but
 *   it is to be interpreted as the bit-level representation of a
 *   single-precision floating point values.
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 30
 *   Rating: 4
 */
unsigned float_i2f(int x) {
  int sign, highbit, exp, frac, flag;
  unsigned temp, result;
  if(!x) // 特判x=0的情况
    return x;             
  sign = (x >> 31) & 1;
  if(sign)
    x =~x + 1; // 获得符号位，并将x变为正值。
   highbit = 0;
  temp = x;
  while(!(temp & 0x80000000))
  {
     temp <<= 1;
     highbit++;
  } //获得x的最高有效位，即确定frac的位数。
  temp = temp << 1;
  exp = 127 + 31 - highbit;  //计算出exp，和frac位数。
  frac = 31 - highbit;
  flag = 0; //进行向偶数舍入
  if((temp & 0x1ff) > 0x100)
  flag = 1; //出现在规定位置后大于0b100的情况就进1.
  if((temp & 0x3ff) == 0x300)
  flag = 1; //出现最后一位为1，且下一位为1的情况也进1(向偶取整)
  result = (sign << 31) + (exp << 23) + (temp >> 9) + flag; //计算最终结果
  return result;
}
